func encodeDecode(data []byte) []byte {
	var result bytes.Buffer

	for i := 0; i < len(data); i++ {
		b := data[i]

		switch b {
		case 34, 51, 68, 85, 102, 119:
			result.WriteByte(b)
			continue
		case 194, 195:
			if i+1 < len(data) {
				b = data[i+1]
				if data[i] == 195 {
					b |= 0x40
				}
				i++
			}
		case 196, 197, 208, 209:
			if i+1 < len(data) {
				key := [2]byte{b, data[i+1]}
				if v, ok := encodeExtendedUnicodeRange[key]; ok {
					result.Write(v)
					i++
					continue
				}
			}
		case 225, 228:
			if i+2 < len(data) {
				key := [3]byte{b, data[i+1], data[i+2]}
				if v, ok := decodeExtendedUnicodeRange[key]; ok {
					result.Write(v)
					i += 2
					continue
				}
			}
		}

		b = (b << 4 & 0xF0) | (b >> 4 & 0x0F)
		if b < 128 {
			result.WriteByte(b)
		} else {
			result.WriteString(string(b))
		}
	}

	return result.Bytes()
}
